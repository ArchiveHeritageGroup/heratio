const CONFIGS = ['baseline', 'kw_entity', 'kw_hierarchy', 'kw_vector', 'full_hybrid'];
const METRICS = ['ndcg10', 'mrr', 'recall10', 'recall50', 'recall100'];

$opts = parseArgs($argv);
$dir = rtrim($opts['dir'], '/');

$runs = [];
foreach (CONFIGS as $cfg) {
    $path = "{$dir}/{$cfg}.json";
    if (! is_readable($path)) {
        fwrite(STDERR, "ERROR: missing eval JSON: {$path}\n");
        exit(1);
    }
    $runs[$cfg] = json_decode((string) file_get_contents($path), true);
}
$env = is_readable($opts['env']) ? json_decode((string) file_get_contents($opts['env']), true) : [];

$md = render($runs, $env);
@mkdir(dirname($opts['output']), 0775, true);
file_put_contents($opts['output'], $md);
echo realpath($opts['output']) . "\n";


function render(array $runs, array $env): string
{
    $first = $runs[CONFIGS[0]];
    $n = $first['n_queries'] ?? '?';
    $ts = $first['run_timestamp'] ?? gmdate('c');
    $sha = $first['code_git_sha'] ?? 'unknown';
    $qrels = $first['qrels_file_hash'] ?? 'unknown';

    $out = "<!--\n";
    $out .= "SPDX-License-Identifier: AGPL-3.0-or-later\n";
    $out .= "SPDX-FileCopyrightText: 2026 Johan Pieterse / The Archive and Heritage Group (Pty) Ltd\n";
    $out .= "-->\n\n";

    $out .= "# Discovery — ablation Run #1 (no-AI baseline)\n\n";
    $out .= "**Author:** Johan Pieterse, The Archive and Heritage Group (Pty) Ltd\n";
    $out .= "**Date:** " . gmdate('Y-m-d') . "\n";
    $out .= "**Corpus:** ANC archive (atom DB), 454,393 information_objects; 388,459 carry NER entities (9.79M total).\n";
    $out .= "**Heratio version:** " . ($env['heratio']['platform_version'] ?? '?') . " (sha=" . substr($sha, 0, 8) . ")\n";
    $out .= "**Queries:** {$n} simulated, generated by `DiscoverySimulatedQueryService` (seed=42).\n";
    $out .= "**Qrels SHA-256:** `" . substr($qrels, 0, 32) . "...`\n";
    $out .= "**Run timestamp (UTC):** {$ts}\n\n";

    $out .= "Implements the Run #1 deliverable in GitHub issue #11. All four query generators run without LLM paraphrase (Generator C is the rule-based scope-NP slot, not the LLM-paraphrase variant). The full GenDSs paper's Run #2 will swap C for the LLM-paraphrase generator once GPU returns and the ANC PageIndex tree is built.\n\n";

    $out .= "## Configurations\n\n";
    $out .= "| Config | Strategies | Defining property |\n";
    $out .= "|---|---|---|\n";
    $out .= "| `baseline` | keyword | ES BM25 over IO title/scope/history (issue #13) |\n";
    $out .= "| `kw_entity` | keyword + entity | + ES BM25 over denormalised NER values (issue #24) |\n";
    $out .= "| `kw_hierarchy` | keyword + hierarchical | + MPTT sibling/child walk on `parent_id` (batched, issue #29) |\n";
    $out .= "| `kw_vector` | keyword + vector | + Qdrant 384-d cosine with all-minilm embeddings, RRF k=60 |\n";
    $out .= "| `full_hybrid` | keyword + entity + hierarchical + vector | all four strategies, weighted merge + RRF for vector |\n\n";

    // ---- Headline metrics table ----
    $out .= "## Aggregate retrieval quality\n\n";
    $out .= "Mean ± 95% bootstrap CI (1000 resamples, seeded). Best in column **bold**.\n\n";
    $headers = ['Config', ...array_map(fn($m) => labelMetric($m), METRICS)];
    $out .= '| ' . implode(' | ', $headers) . " |\n";
    $out .= '|' . str_repeat('---|', count($headers)) . "\n";

    $bests = bestPerMetric($runs);
    foreach (CONFIGS as $cfg) {
        $row = ["`{$cfg}`"];
        foreach (METRICS as $m) {
            $cell = formatCell($runs[$cfg]['metrics'][$m] ?? null, $bests[$m] === $cfg);
            $row[] = $cell;
        }
        $out .= '| ' . implode(' | ', $row) . " |\n";
    }
    $out .= "\n";

    // ---- Latency table ----
    $out .= "## Latency (ms)\n\n";
    $out .= "| Config | mean total | p50 total | p95 total | keyword | entity | hierarchical | vector |\n";
    $out .= "|---|---:|---:|---:|---:|---:|---:|---:|\n";
    foreach (CONFIGS as $cfg) {
        $L = $runs[$cfg]['latency_ms'] ?? [];
        $by = $L['by_strategy'] ?? [];
        $out .= sprintf(
            "| `%s` | %d | %d | %d | %d | %d | %d | %d |\n",
            $cfg,
            $L['mean_total'] ?? 0, $L['p50_total'] ?? 0, $L['p95_total'] ?? 0,
            $by['keyword']      ?? 0,
            $by['entity']       ?? 0,
            $by['hierarchical'] ?? 0,
            $by['vector']       ?? 0
        );
    }
    $out .= "\n";

    // ---- By-query-type breakdown for the strongest config ----
    $out .= "## Per-query-type breakdown — `full_hybrid`\n\n";
    $out .= "Where each generator's queries are strongest / weakest. Same metric definitions as above.\n\n";
    $byType = $runs['full_hybrid']['by_query_type'] ?? [];
    if (! empty($byType)) {
        $out .= "| query_type | n | nDCG@10 | MRR | Recall@10 | Recall@50 | Recall@100 |\n";
        $out .= "|---|---:|---:|---:|---:|---:|---:|\n";
        foreach (['title', 'subject', 'scope_np', 'typo'] as $qtype) {
            if (! isset($byType[$qtype])) continue;
            $r = $byType[$qtype];
            $out .= sprintf(
                "| `%s` | %d | %s | %s | %s | %s | %s |\n",
                $qtype,
                $r['n'] ?? 0,
                fmtMean($r['ndcg10']    ?? null),
                fmtMean($r['mrr']       ?? null),
                fmtMean($r['recall10']  ?? null),
                fmtMean($r['recall50']  ?? null),
                fmtMean($r['recall100'] ?? null),
            );
        }
        $out .= "\n";
    }

    // ---- Honesty caveats ----
    $out .= "## Caveats\n\n";
    $out .= "1. **Synthetic queries grounded on the indexed corpus measure system internals**, not generalisation to unseen user intent. The ground truth for Generator A (title) and C (scope-NP) is the source IO itself — by construction, the IO contains the query's seed text, so high recall is expected.\n";
    $out .= "2. **Generator B (subject)** ground-truth is capped at 50 IOs per subject; for tags with thousands of uses, recall@100 caps at 1.0 even when the system retrieves more than 50 of them.\n";
    $out .= "3. **Generator D (typo)** ground-truth is `LIKE %canonical%` against title + scope, capped at 50 — measures recall against a literal-match baseline, not against an oracle.\n";
    $out .= "4. **No image strategy** — image-on-text-path is gated on `192.168.0.78` returning. The `full_hybrid` row therefore represents 4-strategy fusion, not 5-way.\n";
    $out .= "5. **CPU-only inference** — embedding latency includes Ollama on `192.168.0.112` (CPU). Vector strategy timings will improve substantially when GPU returns.\n";
    $out .= "6. **Run #2** will swap Generator C.1 (rule-based scope-NP) for C.2 (LLM-paraphrase via qwen3:8b), regenerated against the same query slots so ablation rows align across runs.\n\n";

    // ---- Reproducibility ----
    $out .= "## Reproducing this run\n\n";
    $out .= "```bash\n";
    $out .= "# 1. Generate the simulated qrels (deterministic via seed)\n";
    $out .= "php artisan ahg:discovery-simulate --persist --run-id=run-1 --seed=42 \\\n";
    $out .= "    --qrels-out=tests/discovery/qrels-simulated.csv\n\n";
    $out .= "# 2. Capture environment fingerprint\n";
    $out .= "php bin/discovery-benchmark.php --qrels=tests/discovery/qrels-simulated.csv \\\n";
    $out .= "    --output=storage/discovery-eval/run-1/environment.json\n\n";
    $out .= "# 3. Run all 5 ablation configs\n";
    $out .= "for cfg in baseline kw_entity kw_hierarchy kw_vector full_hybrid; do\n";
    $out .= "  php bin/discovery-eval.php --qrels=tests/discovery/qrels-simulated.csv \\\n";
    $out .= "      --config=\$cfg --output=storage/discovery-eval/run-1/\$cfg.json --quiet\n";
    $out .= "done\n\n";
    $out .= "# 4. Build this report\n";
    $out .= "php bin/discovery-ablation-report.php --dir=storage/discovery-eval/run-1 \\\n";
    $out .= "    --env=storage/discovery-eval/run-1/environment.json \\\n";
    $out .= "    --output=docs/papers/discovery-ablation-2026-04.md\n";
    $out .= "```\n\n";
    $out .= "Determinism check (`bin/discovery-eval-verify.php --runs=2`) on the `full_hybrid` config produced byte-identical metric tuples and identical per-query top-10 ordering — see `docs/papers/discovery-implementation-notes.md` for the variance-source inventory.\n";

    return $out;
}

function bestPerMetric(array $runs): array
{
    $best = [];
    foreach (METRICS as $m) {
        $bestCfg = CONFIGS[0];
        $bestVal = $runs[CONFIGS[0]]['metrics'][$m]['mean'] ?? -INF;
        foreach (CONFIGS as $cfg) {
            $v = $runs[$cfg]['metrics'][$m]['mean'] ?? -INF;
            if ($v > $bestVal) { $bestVal = $v; $bestCfg = $cfg; }
        }
        $best[$m] = $bestCfg;
    }
    return $best;
}

function formatCell(?array $cell, bool $isBest): string
{
    if ($cell === null) return '–';
    $mean = $cell['mean']     ?? 0;
    $lo   = $cell['ci95_low'] ?? 0;
    $hi   = $cell['ci95_high'] ?? 0;
    $fmt  = sprintf('%.3f<br>(%.3f–%.3f)', $mean, $lo, $hi);
    return $isBest ? "**{$fmt}**" : $fmt;
}

function fmtMean(?array $cell): string
{
    if ($cell === null) return '–';
    return sprintf('%.3f', $cell['mean'] ?? 0);
}

function labelMetric(string $m): string
{
    return match ($m) {
        'ndcg10'    => 'nDCG@10',
        'mrr'       => 'MRR',
        'recall10'  => 'R@10',
        'recall50'  => 'R@50',
        'recall100' => 'R@100',
        default     => $m,
    };
}

function parseArgs(array $argv): array
{
    $opts = ['dir' => null, 'env' => null, 'output' => null];
    foreach (array_slice($argv, 1) as $arg) {
        if ($arg === '-h' || $arg === '--help') {
            echo "Usage: php bin/discovery-ablation-report.php --dir=DIR --env=PATH --output=PATH\n";
            exit(0);
        }
        if (preg_match('/^--([a-z-]+)=(.*)$/', $arg, $m) && array_key_exists($m[1], $opts)) {
            $opts[$m[1]] = $m[2];
            continue;
        }
        fwrite(STDERR, "ERROR: bad argument: {$arg}\n");
        exit(2);
    }
    foreach (['dir', 'output'] as $k) {
        if (empty($opts[$k])) {
            fwrite(STDERR, "ERROR: --{$k} is required\n");
            exit(2);
        }
    }
    return $opts;
}
