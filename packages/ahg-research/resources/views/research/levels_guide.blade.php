      <div class="modal-body">
        <p>This guide explains the three modes available in the Research sidebar and suggests a short workflow for each.</p>

        <h4>Beginning</h4>
        <p>For new or occasional researchers who want to quickly create a project and collect a few key items. Suggested steps:</p>
        <ol>
          <li>Create a Project (title + brief abstract)</li>
          <li>Import 3–5 references into Bibliography</li>
          <li>Upload 1–2 sources and save to a Collection</li>
          <li>Make notes in a Notebook entry</li>
          <li>Export a bibliography for your draft</li>
        </ol>

        <h4>Intermediate</h4>
        <p>For ongoing research projects. Suggested steps:</p>
        <ol>
          <li>Set up Project metadata and DMP stub</li>
          <li>Ingest and tag sources; capture bibliographic metadata</li>
          <li>Record claims with supporting evidence and link sources</li>
          <li>Draft sections in the Writing Studio</li>
          <li>Run Contradiction Engine as needed</li>
        </ol>

        <h4>Advanced</h4>
        <p>For publication-ready and reproducible research. Suggested steps:</p>
        <ol>
          <li>Finalize manuscript sections and accept any AI-assisted drafts</li>
          <li>Run the Contradiction Engine and resolve flagged conflicts</li>
          <li>Prepare a Replication Pack with data and code</li>
          <li>Complete ethics approvals and link them in the project</li>
          <li>Publish outputs and link to repository; track impact</li>
        </ol>

        <h5>Quick tips</h5>
        <ul>
          <li>Share notebooks for collaborative drafting in Intermediate mode.</li>
          <li>Use AI Drafts as proposals only — always review before accepting.</li>
          <li>Keep provenance: every AI suggestion is logged to the ai_provenance table.</li>
        </ul>
